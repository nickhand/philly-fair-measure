"""Leakage-safe renovation-episode probabilities and valuation challengers.

A flip is a sequence, not a property characteristic: acquisition, permits,
completion, and resale.  This module reconstructs that sequence from exact
event dates, learns the probability of a large time-adjusted recovery on
historical repeat sales, and tests whether the probability improves valuation
or prediction-risk information on an untouched future slice.

The auxiliary target may use the resale price, but that price never enters an
input feature.  Fit-row probabilities are generated by expanding-time
cross-fitting; validation and test probabilities come from labels completed in
the fit period only.  Later permits never appear in an earlier row.
"""

